#인댁싱 혼합 불가 만약 하더라도 앞에 인덱스사용해야함

print("{0:<20}"  .format('hi')) #부등호 방향에 정렬!!
print("{0:>20}"  .format('hi')) #오른쪽
print("{0:^20}"  .format('hi')) #가운제
print("{0:*^20}"  .format('hi')) #가운데 정렬 + 빈공간은 '-'으로 체움
